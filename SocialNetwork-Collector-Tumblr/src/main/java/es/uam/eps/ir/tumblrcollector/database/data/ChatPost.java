package es.uam.eps.ir.tumblrcollector.database.data;

/**
 *
 * @author Javier
 */
public class ChatPost extends Post
{
    String title;
    String body;

    public ChatPost()
    {
        this.setType(TumblrType.CHAT_POST);
    }
    
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
    
    
}
